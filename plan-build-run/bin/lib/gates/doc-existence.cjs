'use strict';

/**
 * Blocking gate: warns when a PBR project is missing PROJECT.md or REQUIREMENTS.md.
 * These files are created by /pbr:begin. If they are absent on a project that has
 * STATE.md + ROADMAP.md, the user likely needs to run /pbr:begin first.
 * Returns { block: true, reason } when docs are missing.
 */

const fs = require('fs');
const path = require('path');
const { readActiveSkill } = require('./helpers.cjs');

/**
 * Check whether PROJECT.md and REQUIREMENTS.md exist in .planning/ for an
 * established PBR project (one that already has STATE.md + ROADMAP.md).
 *
 * @param {object} _data - hook data (unused, kept for consistent gate signature)
 * @returns {{ block: true, reason: string }|null}
 */
function checkDocExistence(_data) {
  const cwd = process.env.PBR_PROJECT_ROOT || process.cwd();
  const planningDir = path.join(cwd, '.planning');

  // Only block for plan and build active skills
  const activeSkill = readActiveSkill(planningDir);
  if (activeSkill !== 'plan' && activeSkill !== 'build') return null;

  // Only block if this is an established PBR project (has STATE.md + ROADMAP.md)
  const statePath = path.join(planningDir, 'STATE.md');
  const roadmapPath = path.join(planningDir, 'ROADMAP.md');
  if (!fs.existsSync(statePath) || !fs.existsSync(roadmapPath)) return null;

  const missing = [];
  if (!fs.existsSync(path.join(planningDir, 'PROJECT.md'))) missing.push('PROJECT.md');
  if (!fs.existsSync(path.join(planningDir, 'REQUIREMENTS.md'))) missing.push('REQUIREMENTS.md');

  if (missing.length === 0) return null;

  return {
    block: true,
    reason: `Missing planning documents: ${missing.join(', ')}.\n\nThese files are generated by /pbr:begin and document project vision and requirements.\n\nRun /pbr:begin to generate them, or create them manually from the templates at plan-build-run/templates/.`
  };
}

module.exports = { checkDocExistence };
